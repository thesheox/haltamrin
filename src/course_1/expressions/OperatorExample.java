package course_1.expressions;

public class OperatorExample {
    public static void main(String[] args) {
        int x = 10, y = 20;
        System.out.println("Sum: " + (x + y));        // Addition
        System.out.println("Is x greater than y? " + (x > y)); // Relational
        System.out.println("Logical AND: " + (x > 5 && y > 15)); // Logical
    }
}
